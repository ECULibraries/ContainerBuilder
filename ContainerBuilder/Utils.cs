        public static TitleDate GetTitleDate(string title, string number, string label)
        {
            var titleDate = new TitleDate();

            if (title == null)
            {
                titleDate.title = label + " " + number.Trim();
            }
            else
            {
                title = title.Replace("’", "'");
                title = title.Replace("“", "\"");
                title = title.Replace("”", "\"");
                title = Regex.Replace(title, @"(<lb />)", ", ");
                title = Regex.Replace(title, @"(<lb/>)", ", ");
                title = Regex.Replace(title, @"(<unitdate.*?>)", "");
                title = Regex.Replace(title, @"(<\/unitdate>)", "");

                title = Regex.Replace(title.Trim().TrimEnd('.'), @"\s{2,}", " ");
                //Check for date, add node if found
                var regex01 = new Regex(@"(.*)([,.] [0-9]{4}-[0-9]{4})$"); // , 1819-1861 OR . 1819-1861
                var regex02 = new Regex(@"(.*)(. (January?|February?|March?|April?|May|June?|July?|August?|September?|October?|November?|December?)\s+\d{1,2},\s+\d{4})$"); // . May 15, 1863
                var regex03 = new Regex(@"(.*)([,.] [0-9]{4}-[0-9]{4}, undated)$"); // Correspondence, 1862-1871, undated
                var regex04 = new Regex(@"(.*)([,.] <unitdate.*?>)(.*)(<\/unitdate>)");
                var regex05 = new Regex(@"(.*)(. (January?|February?|March?|April?|May|June?|July?|August?|September?|October?|November?|December?))(\s+\d{4})$"); // . May 1863
                var regex06 = new Regex(@"(.*)([,.] [0-9]{4}-[0-9]{4}), ([0-9]{4})$"); // ow III. 1870-1871, 1881
                var match01 = regex01.Match(title);
                var match02 = regex02.Match(title);
                var match03 = regex03.Match(title);
                var match04 = regex04.Match(title);
                var match05 = regex05.Match(title);
                var match06 = regex06.Match(title);
                if (match01.Success)
                {
                    titleDate.title = match01.Groups[1].Value;
                    titleDate.expression = match01.Groups[2].Value.Substring(2, match01.Groups[2].Value.Length - 2);
                    titleDate.type = "903";//"inclusive";
                    titleDate.start = match01.Groups[2].Value.Substring(2, 4);
                    titleDate.end = match01.Groups[2].Value.Substring(7, 4);

                }
                else if (match02.Success)
                {
                    titleDate.title = match02.Groups[1].Value;
                    titleDate.expression = match02.Groups[2].Value.Substring(2, match02.Groups[2].Value.Length - 2);
                    titleDate.type = "901";//"single";
                }
                else if (match03.Success) // Correspondence, 1862-1871, undated
                {
                    titleDate.title = match03.Groups[1].Value;

                    var date = match03.Groups[2].Value.Substring(2, match03.Groups[2].Value.Length - 2).Replace(", undated", "");
                    titleDate.expression = date;
                    titleDate.type = "903";//"inclusive";
                    titleDate.start = date.Substring(0, 4);
                    titleDate.end = date.Substring(5, 4);

                }
                else if (match04.Success)
                {
                    titleDate.title = match04.Groups[1].Value;


                    if (match04.Groups[3].Value.Length == 9 && match04.Groups[3].Value[4] == '-')
                    {
                        titleDate.type = "903";//"inclusive";
                        titleDate.start = match04.Groups[2].Value.Substring(0, 4);
                        titleDate.end = match04.Groups[2].Value.Substring(5, 4);
                    }

                    titleDate.expression = match04.Groups[3].Value;

                }
                else if (match05.Success)
                {
                    titleDate.title = match05.Groups[1].Value;
                    titleDate.expression = match05.Groups[2].Value.Substring(2, match05.Groups[2].Value.Length - 2) + match05.Groups[4].Value;
                    titleDate.type = "901";//"single";
                }
                else if (match06.Success)
                {
                    titleDate.title = match06.Groups[1].Value;
                    titleDate.expression = match06.Groups[2].Value.Substring(2, match06.Groups[2].Value.Length - 2) + ", " + match06.Groups[3].Value;
                    titleDate.type = "903";//"inclusive";
                    titleDate.start = "1870";
                    titleDate.end = "1881";
                }
                else
                {
                    titleDate.title = title;
                }
            }

            titleDate.title = titleDate.title.Replace("'", "''");
            return titleDate;
        }

